#include "Pathing/Point.h"


//*****************************************************************************
Pathing::Point::Point()
{

}

//*****************************************************************************
Pathing::Point::Point
(
  uint16_t x,
  uint16_t y,
  uint16_t z
)
:m_x(x),
 m_y(y),
 m_z(z)
{

}


//*****************************************************************************
Pathing::Point::~Point()
{

}


//*****************************************************************************
bool Pathing::Point::operator==
(
  const Pathing::Point& p
)
{
  return m_x == p.m_x \
    && m_y == p.m_y \
    && m_z == p.m_z;
}


//*****************************************************************************
bool Pathing::Point::valid()
{
  return m_z > 0;
}


//*****************************************************************************
const uint16_t& Pathing::Point::xGet() const
{
  return m_x;
}


//*****************************************************************************
void Pathing::Point::xSet
(
  uint16_t x
)
{
  m_x = x;
}


//*****************************************************************************
const uint16_t& Pathing::Point::yGet() const
{
  return m_y;
}


//*****************************************************************************
void Pathing::Point::ySet
(
  uint16_t y
)
{
  m_y = y;
}


//*****************************************************************************
const uint16_t& Pathing::Point::zGet() const
{
  return m_z;
}


//*****************************************************************************
void Pathing::Point::zSet
(
  uint16_t z
)
{
  m_z = z;
}
