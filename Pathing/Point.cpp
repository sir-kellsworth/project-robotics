#include "Pathing/Point.h"


//*****************************************************************************
Pathing::Point::Point()
{
  
}

//*****************************************************************************
Pathing::Point::Point
(
  float x,
  float y,
  float z
)
:m_x(x),
 m_y(y),
 m_z(z)
{

}


//*****************************************************************************
Pathing::Point::~Point()
{

}


//*****************************************************************************
bool Pathing::Point::valid()
{
  return m_z > 0;
}


//*****************************************************************************
const float& Pathing::Point::xGet() const
{
  return m_x;
}


//*****************************************************************************
void Pathing::Point::xSet
(
  float x
)
{
  m_x = x;
}


//*****************************************************************************
const float& Pathing::Point::yGet() const
{
  return m_y;
}


//*****************************************************************************
void Pathing::Point::ySet
(
  float y
)
{
  m_y = y;
}


//*****************************************************************************
const float& Pathing::Point::zGet() const
{
  return m_z;
}


//*****************************************************************************
void Pathing::Point::zSet
(
  float z
)
{
  m_z = z;
}
